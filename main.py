        with open (path, "r") as f:
            grid = []
            for l in f:
                ligne = []
                for c in l:
                    if c == "0":
                        ligne.append(0)
                    if c == "1":
                        ligne.append(1)
                    if c == "2":
                        ligne.append(2)

                grid.append(ligne)
            f.close()
            return grid
    
